#ifndef pPIMSUBTARGET_H
#define pPIMSUBTARGET_H

#include "pPIMFrameLowering.h"
#include "pPIMISelLowering.h"
#include "pPIMInstrInfo.h"
#include "llvm/CodeGen/SelectionDAGTargetInfo.h"
#include "llvm/IR/DataLayout.h"
#include "llvm/Target/TargetMachine.h"
#include "llvm/Target/TargetSubtargetInfo.h"

#define GET_SUBTARGETINFO_HEADER
#include "pPIMGenSubtargetInfo.inc"

namespace llvm {
class StringRef;

class pPIMSubtarget : public pPIMGenSubtargetInfo {
  virtual void anchor();

  pPIMInstrInfo InstrInfo;
  pPIMFrameLowering FrameLowering;
  pPIMTargetLowering TLInfo;

public:
  // Initializes the data members to match that of the specified triple.
  pPIMSubtarget(const Triple &TT, StringRef CPU,
               StringRef FS, pPIMTargetMachine &TM);

  // Parses features string setting specified subtarget options. The
  // definition of this function is auto-generated by tblgen.
  void ParseSubtargetFeatures(StringRef CPU, StringRef FS);

  const pPIMFrameLowering *getFrameLowering() const override {
    return &FrameLowering;
  }
  const pPIMInstrInfo *getInstrInfo() const override {
  	return &InstrInfo;
  }
  const TargetRegisterInfo *getRegisterInfo() const override {
    return &InstrInfo.getRegisterInfo();
  }
  const pPIMTargetLowering *getTargetLowering() const override {
    return &TLInfo;
  }
};
} // End llvm namespace

#endif
